package org.activiti.rest.api.cycle.dto;

/**
 * @author Nils Preusker
 */
public class ContentView {

  private String key;
  private String name;
  private String value;

  public ContentView() {
    super();
    this.key = "";
    this.name = "";
    this.value = "";
  }

  public ContentView(String key, String name, String value) {
    super();
    this.key = key;
    this.name = name;
    this.value = value;
  }

  public String getKey() {
    return key;
  }

  public void setKey(String key) {
    this.key = key;
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public String getValue() {
    return value;
  }

  public void setValue(String value) {
    this.value = value;
  }

}
